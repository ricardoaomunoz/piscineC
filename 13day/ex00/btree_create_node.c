#include "ft_btree.h"
#include <stdlib.h>

t_btree	*btree_create_node(void *item)
{
	t_btree	*elem;
	elem = malloc(sizeof(b_tree));
	elem = item;
	elem->left = NULL;
	elem->right = NULL;
}
