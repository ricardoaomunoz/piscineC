int		max(int* tab, unsigned int len)
{
	int ans;
	unsigned int i;

	ans = 0;
	i = 0;
	while (i < len)
	{
		ans = ans > tab[i] ? ans : tab[i];
		i++;
	}
	return (ans);
}
